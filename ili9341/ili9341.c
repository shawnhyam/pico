void place_metatile_at(int x, int y, uint8_t palette, uint8_t tiles[4]) {
    set_tile_at(tiles[0], palette, x*2, y*2);
    set_tile_at(tiles[2], palette, x*2+1, y*2);
    set_tile_at(tiles[1], palette, x*2, y*2+1);
    set_tile_at(tiles[3], palette, x*2+1, y*2+1);

}

void place_bush_at(int x, int y, int width) {
    // .db $24, $24, $24, $35 ;bush left
    //  .db $36, $25, $37, $25 ;bush middle
    //  .db $24, $38, $24, $24 ;bush right
    place_metatile_at(x, y, 0, (uint8_t[4]){0x24, 0x24, 0x24, 0x35});
    for (int i=0; i<width; i++) {
        place_metatile_at(x+1+i, y, 0, (uint8_t[4]){0x36, 0x25, 0x37, 0x25});
    }
    place_metatile_at(x+width+1, y, 0, (uint8_t[4]){0x24, 0x38, 0x24, 0x24});
}

void place_pipe_at(int x, int y, int height) {
    /*
     .db $60, $64, $61, $65 ;decoration pipe end left, points up
     .db $62, $66, $63, $67 ;decoration pipe end right, points up
     .db $68, $68, $69, $69 ;pipe shaft left
     .db $26, $26, $6a, $6a ;pipe shaft right
     */
    
    for (int i=0; i<height; i++) {
        place_metatile_at(x, y-i, 0, (uint8_t[4]){0x68, 0x68, 0x69, 0x69});
        place_metatile_at(x+1, y-i, 0, (uint8_t[4]){0x26, 0x26, 0x6a, 0x6a});
    }
    place_metatile_at(x, y-height, 0, (uint8_t[4]){0x60, 0x64, 0x61, 0x65});
    place_metatile_at(x+1, y-height, 0, (uint8_t[4]){0x62, 0x66, 0x63, 0x67});

}


// x is the center, y is the top
void place_mountaintop_at(int x, int y) {
    /*
    .db $24, $30, $30, $26 ;mountain left
     .db $26, $26, $34, $26 ;mountain left bottom/middle center
     .db $24, $31, $24, $32 ;mountain middle top
     .db $33, $26, $24, $33 ;mountain right
     */
    place_metatile_at(x, y, 0, (uint8_t[4]){0x24, 0x31, 0x24, 0x32});
    place_metatile_at(x, y+1, 0, (uint8_t[4]){0x26, 0x26, 0x34, 0x26});
    place_metatile_at(x-1, y+1, 0, (uint8_t[4]){0x24, 0x30, 0x30, 0x26});
    place_metatile_at(x+1, y+1, 0, (uint8_t[4]){0x33, 0x26, 0x24, 0x33});

    
}

