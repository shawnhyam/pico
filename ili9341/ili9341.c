void draw_background() {
    ili9341_set_command(ILI9341_RAMWR);

    for (int x=0; x<40; x++) {
        uint16_t buffer[8*240];  // 8 pixels wide, 240 pixels tall
        int buffer_idx = 0;

        for (int x_offset=0; x_offset<8; x_offset++) {
            
            for (int y=29; y>=0; y--) {
                Tile *tile = tile_at(x, y);
                uint8_t tile_palette_idx = get_tile_palette_at(x, y);
                uint16_t *tile_palette = palette[tile_palette_idx];
                
                for (int i=0; i<8; i++) {
                    uint8_t palette_index = 0;
                    for (int bit=2; bit>=0; bit--) {
                        palette_index <<= 1;
                        palette_index |= ((tile->mem[bit*8+(7-i)] >> (7-x_offset)) & 1);
                    }
                    // look up the color from the palette
                    // color 0 is "transparent", will show global background color instead
                    uint16_t color = palette_index ? tile_palette[palette_index] : global_background;
                    
                    // this color is pre-byteswapped and blue-red swapped
                    buffer[buffer_idx++] = color;

                }
                
            }
        }
        
        // write out this data
        ili9341_write_data(buffer, 8*240*2);
    }
}

